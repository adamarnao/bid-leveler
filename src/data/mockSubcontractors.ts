function vendor(seed: VendorSeed): Subcontractor {
  const phoneSuffix = seed.id
    .split("")
    .reduce((total, character) => total + character.charCodeAt(0), 0)
    .toString()
    .slice(-4)
    .padStart(4, "0");
  const contactSlug = seed.contactName.toLowerCase().replace(/\s+/g, ".");

  return {
    id: seed.id,
    companyName: seed.companyName,
    dba: seed.dba,
    address: {
      line1: `${1000 + Number(phoneSuffix)} Commerce Blvd`,
      city: seed.city,
      state: "FL",
      zip: `34${phoneSuffix.slice(0, 3)}`,
    },
    website: `https://example.com/${seed.id.replace("sub-", "")}`,
    mainPhone: `941-555-${phoneSuffix}`,
    notes: seed.notes,
    serviceArea: {
      states: ["FL"],
      counties: uniqueStrings([seed.county, "Sarasota", "Manatee"]),
      citiesOrMarkets: uniqueStrings([seed.city, "Sarasota", "Bradenton"]),
      travelRadiusMiles: seed.willTravel === false ? 45 : 90,
      willTravel: seed.willTravel ?? true,
    },
    locations: seed.locations,
    contacts: [
      {
        id: `${seed.id}-primary-contact`,
        role: "ESTIMATOR",
        name: seed.contactName,
        title: seed.contactTitle ?? "Estimator",
        email: `${contactSlug}@example.com`,
        officePhone: `941-555-${phoneSuffix}`,
        mobilePhone: `941-555-${String(Number(phoneSuffix) + 1111).slice(-4)}`,
        primaryPhoneType: "MOBILE",
        isPrimary: true,
        isDefaultInviteRecipient: seed.extraContacts ? false : true,
        active: true,
      },
      ...(seed.extraContacts ?? []),
    ],
    primaryDivisionId: seed.primaryDivisionId,
    csiCoverage: {
      sourceVersion: seed.sourceVersion ?? "MASTERFORMAT_2004_PLUS",
      divisionIds: seed.divisionIds,
      sectionIds: seed.sectionIds,
      specialtyScopeNotes: seed.specialtyScopeNotes,
    },
    prequalification: {
      status: seed.prequalificationStatus,
      w9OnFile: seed.w9OnFile ?? true,
      insuranceOnFile: seed.insuranceOnFile ?? true,
      licenseOnFile: seed.licenseOnFile ?? true,
      bondingCapacity: seed.bondingCapacity,
      insuranceExpirationDate: seed.insuranceExpirationDate,
      licenseExpirationDate: seed.licenseExpirationDate,
    },
    vpi: buildVpi(seed.vpiOverall, seed.projectsEvaluated, seed.confidenceLevel),
    relationshipStatus: seed.relationshipStatus,
    createdDate: "2026-06-01",
    updatedDate: "2026-06-11",
    archived: false,
  };
}

function location(
  id: string,
  name: string,
  type: SubcontractorLocation["type"],
  city: string,
  county: string,
  isPrimary = false
): SubcontractorLocation {
  return {
    id,
    name,
    type,
    address: {
      line1: `${1200 + id.length} Regional Pkwy`,
      city,
      state: "FL",
      zip: `34${String(id.length * 37).padStart(3, "0").slice(0, 3)}`,
    },
    mainPhone: `941-555-${String(id.length * 173).padStart(4, "0").slice(-4)}`,
    serviceArea: {
      states: ["FL"],
      counties: uniqueStrings([county, "Sarasota", "Manatee"]),
      citiesOrMarkets: uniqueStrings([city, "Sarasota", "Bradenton"]),
      travelRadiusMiles: type === "FIELD_OFFICE" ? 60 : 90,
      willTravel: true,
    },
    isPrimary,
  };
}

function contact(contactValue: SubcontractorContact): SubcontractorContact {
  return {
    active: true,
    primaryPhoneType: "MOBILE",
    ...contactValue,
  };
}

function buildVpi(
  overall: number | undefined,
  projectsEvaluated: number,
  confidenceLevel: VpiConfidenceLevel
) {
  if (overall === undefined) {
    return {
      overall,
      projectsEvaluated,
      confidenceLevel,
    };
  }

  return {
    responsiveness: clampRating(overall + 0.2),
    bidCompleteness: clampRating(overall),
    bidAccuracy: clampRating(overall - 0.1),
    schedulePerformance: clampRating(overall - 0.2),
    fieldQuality: clampRating(overall + 0.1),
    administrativeCompliance: clampRating(overall - 0.1),
    overall,
    projectsEvaluated,
    confidenceLevel,
  };
}

function clampRating(value: number) {
  return Math.max(1, Math.min(5, Number(value.toFixed(1))));
}

function uniqueStrings(values: string[]) {
  return Array.from(new Set(values.filter(Boolean)));
}
